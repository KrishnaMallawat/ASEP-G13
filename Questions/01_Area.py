# AREA OF TRIANGLE
b = int(input("Enter the base of the triangle: "))
h = int(input("Enter the height of the triangle: "))
area = 0.5 * b * h
print(f"Area of the triangle : {area}\n")

# AREA OF CIRCLE
r = float(input("Enter the radius of the circle: "))
pie = 3.14
area = pie * r * r
print("Area of the circle :" , area)